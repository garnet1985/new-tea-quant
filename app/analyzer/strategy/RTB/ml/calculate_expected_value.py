# 盈亏平衡点：期望值 = 0
# p × 盈利 + (1-p) × 亏损 = 0
# p × 盈利 - (1-p) × |亏损| = 0
# p × (盈利 + |亏损|) = |亏损|
# p = |亏损| / (盈利 + |亏损|)

所需胜率 = abs(平均亏损) / (平均盈利 + abs(平均亏损))

print(f'\n盈亏平衡点胜率:')
