# 凯利公式
# f = (p*b - q) / b
# p = 胜率, q = 败率, b = 盈亏比
p = 盈利率
q = 亏损率
b = 盈亏比

凯利比例 = (p * b - q) / b

print(f'\n凯利公式:')
print(f'  f = (p×b - q) / b')
print(f'  p (胜率) = {p:.4f}')
print(f'  q (败率) = {q:.4f}')
print(f'  b (盈亏比) = {b:.4f}')
print(f'  f = ({p:.4f} × {b:.4f} - {q:.4f}) / {b:.4f}')
print(f'    = {凯利比例:.4f}')
print(f'    = {凯利比例*100:.2f}%')

print(f'\n解读:')
if 凯利比例 > 0:
    print(f'  ✅ 正凯利值 = 值得投资')
